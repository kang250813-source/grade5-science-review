function getAllTopicIds() {
  return UNITS.flatMap(u => u.topics.map(t => t.id));
}

function getTopicById(id) {
  for (const unit of UNITS) {
    const topic = unit.topics.find(t => t.id === id);
    if (topic) return { unit, topic };
  }
  return null;
}

function getTotalPoints() {
  return UNITS.reduce((sum, u) => sum + u.topics.reduce((s, t) => s + t.points.length, 0), 0);
}
