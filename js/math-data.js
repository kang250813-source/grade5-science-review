function getMathAllTopicIds() {
  return MATH_UNITS.flatMap(u => u.topics.map(t => t.id));
}

function getMathTopicById(id) {
  for (const u of MATH_UNITS) {
    const t = u.topics.find(x => x.id === id);
    if (t) return { unit: u, topic: t };
  }
  return null;
}

function getMathTotalPoints() {
  return MATH_UNITS.reduce((n, u) => n + u.topics.reduce((m, t) => m + t.points.length, 0), 0);
}
