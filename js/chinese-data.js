function getChnAllTopicIds() {
  return CHN_UNITS.flatMap(u => u.topics.map(t => t.id));
}

function getChnTopicById(id) {
  for (const u of CHN_UNITS) {
    const t = u.topics.find(x => x.id === id);
    if (t) return { unit: u, topic: t };
  }
  return null;
}

function getChnTotalPoints() {
  return CHN_UNITS.reduce((n, u) => n + u.topics.reduce((m, t) => m + t.points.length, 0), 0);
}
